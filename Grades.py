marks = int(input("Enter Students score"))
if 80 < marks <= 100:
    print("You scored an A")
elif 60 < marks <= 80:
    print("You scored a B")
elif 40 < marks <= 60:
    print("You scored a C")
elif 30 < marks <= 40:
    print("You scored a D")
elif 0 < marks <= 30:
    print("Sorry You Failed")
else:
    print("Wrong input")
